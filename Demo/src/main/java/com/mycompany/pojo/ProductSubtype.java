package com.mycompany.pojo;

/**
 *
 * @author HIEN
 */
public class ProductSubtype {
    private int id;
    private String name;
    private int typeId;

    public ProductSubtype(){
        
    }
    
    public ProductSubtype(int id, String name, int typeId){
        this.id = id;
        this.name = name;
        this.typeId = typeId;
    }
    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the typeId
     */
    public int getTypeId() {
        return typeId;
    }

    /**
     * @param typeId the typeId to set
     */
    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }
    
}
